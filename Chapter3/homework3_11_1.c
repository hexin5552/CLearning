#include <stdio.h>
int main(void) {
    //整数上溢
    int a = 2147483647;
    printf("a = %d\n", a);//整数最大值
    printf("a + 1 = %d\n",a + 1);//整数上溢
    printf("a + 2 = %d\n",a + 2);//整数继续上溢
//整数以补码存储，当超过最大值时，高位溢出被截断，符号位反转（从0变1），结果从最小值（-2147483648）开始循环
//无符号整数（如unsigned int）上溢时，结果从0开始循环（如4294967295 + 1 = 0）

    //浮点数上溢
    float b = 3.4e38 * 100.0f;
    printf("b = %e\n", b);
//浮点数上溢时，结果超出其最大表示范围，系统返回无穷大（inf）或负无穷大（-inf）
//若使用double类型（最大值约为1.7e308），上溢同样返回inf

    //浮点数下溢
    float c = 1.14e-45 / 10.0f;
    printf("c = %e\n", c);
//浮点数下溢时，数值小于最小正规格式化数，系统将其表示为非格式化数（精度损失）或直接归零
//非格式化数保留部分精度，但指数为最小值，尾数部分失去隐含的最高位1，导致精度下降
    return 0;
}